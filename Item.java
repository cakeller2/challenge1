package challenge1;

public class Item {
    
    String itemName;
    String category;
    String name;
    String email;
    double price;

    public Item(String itemName, String category, String name, String email, double price){

        this.itemName = itemName;
        this.category = category;
        this.name = name;
        this.email = email;
        this.price = price;
    }
}
